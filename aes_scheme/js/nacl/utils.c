/*
generate random string of len size
 */
char *rand_string(char *str, size_t size)
{
   static int mySeed = 123456;
   srand(time(NULL) * size + ++mySeed);
   const char charset[] = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJK...";
   if (size) {
     --size;
     size_t n;
     for (n = 0; n < size; n++) {
         int key = rand() % (int) (sizeof charset - 1);
         str[n] = charset[key];
     }
     str[size] = '\0';
   }
   return str;
}

/*
Convert the ascii string to ciphertext of (unsigned char*)
*/
unsigned char* convert_string_to_ascii(char* ascii_cipher, int* ret_len){
   int ilen = strlen(ascii_cipher)/2;
   unsigned char* ciphertext = (unsigned char*) malloc(ilen);
   int len = 0;
   char* pch;
   pch = strtok (ascii_cipher," ");

   while (pch != NULL){      
      int i = atoi(pch);
      ciphertext[len++] = (unsigned char) i;
      pch = strtok (NULL, " ");
   }
   ciphertext = (unsigned char*) realloc(ciphertext, len);
   *ret_len = len;
   return ciphertext;
}

/*
Convert the ciphertext to ascii string
*/
char* convert_ascii_to_string(unsigned char* ciphertext, int len){
   int j;    
   char* ascii_cipher = (char*) malloc(len*4 + 11);
   strcpy(ascii_cipher, "");
   for (j=0; j < len; j++){
     char* tmp = malloc(5);
     sprintf(tmp, "%d ", ciphertext[j]);
     strcat(ascii_cipher, tmp);
   }
   return ascii_cipher;
}

